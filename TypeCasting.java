class TypeCasting
{
    public static void main(String[] args)
    {
        int a=257;
        byte k=(byte)a;
      System.out.println(k);

      int x=32768;
      byte y=(byte) x;
      System.out.println(y);

      byte c=10;
      byte b=30;
      int result1=c*b;
      System.out.println(result1);
    }
}